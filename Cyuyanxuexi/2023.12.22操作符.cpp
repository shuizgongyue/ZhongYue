//    ====   二分查找 ====== 
//    给定一个数组 接收一个用户输入的数字 在数组里面进行查早 找到了返回数组对应值的下标 否则返回NO
int erfen(int arr[],int sz,int inp)
{
	//确定左右下标
	int zuo = 0;
	int yu = sz - 1;

	while (zuo<=yu)
	{
		int zg = (zuo + yu) / 2;
		if (arr[zg] > inp)
		{
			yu = zg - 1;
		}
		else if (arr[zg] < inp)
		{
			zuo = zg + 1;
		}
		else
			return zg;
	}
	return -1;
}
