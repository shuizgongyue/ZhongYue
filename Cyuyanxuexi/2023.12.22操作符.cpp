void maopao(int arr[], int sz)
{
	int i = 0;
	for (i = 0; i < sz-1; i++)//确定比较的趟数
	{
		int j = 0;
		for (j = 0; j < sz - 1 - i; j++)//一趟比较需要比较多少对
		{
			if (arr[j] > arr[j + 1])
			{
				int map = 0;
				map = arr[j];
				arr[j] = arr[j + 1];
				arr[j + 1] = map;
			}
		}
	}
}
